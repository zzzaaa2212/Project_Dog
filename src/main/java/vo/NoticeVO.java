package vo;

public class NoticeVO {
	
	private int notice_idx, notice_readhit, notice_del_info;
	private String notice_name, notice_pwd, notice_regdate, notice_content, notice_title;
	
	public int getNotice_idx() {
		return notice_idx;
	}
	public void setNotice_idx(int notice_idx) {
		this.notice_idx = notice_idx;
	}
	public int getNotice_readhit() {
		return notice_readhit;
	}
	public void setNotice_readhit(int notice_readhit) {
		this.notice_readhit = notice_readhit;
	}
	public int getNotice_del_info() {
		return notice_del_info;
	}
	public void setNotice_del_info(int notice_del_info) {
		this.notice_del_info = notice_del_info;
	}
	public String getNotice_name() {
		return notice_name;
	}
	public void setNotice_name(String notice_name) {
		this.notice_name = notice_name;
	}
	public String getNotice_pwd() {
		return notice_pwd;
	}
	public void setNotice_pwd(String notice_pwd) {
		this.notice_pwd = notice_pwd;
	}
	public String getNotice_regdate() {
		return notice_regdate;
	}
	public void setNotice_regdate(String notice_regdate) {
		this.notice_regdate = notice_regdate;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	
	
}
